import { Helmet } from "react-helmet-async";

interface SEOProps {
  title?: string;
  description?: string;
  keywords?: string;
  image?: string;
  url?: string;
  type?: "website" | "article";
  author?: string;
  publishedTime?: string;
  modifiedTime?: string;
  section?: string;
  tags?: string[];
}

export default function SEO({
  title = "Glucose Tracker - Diabetes Management Made Simple",
  description = "Track your blood glucose levels, visualize trends, and manage your diabetes effectively. Modern, mobile-first diabetes management app with secure authentication and real-time insights.",
  keywords = "diabetes, glucose tracker, blood sugar, diabetes management, health app, glucose monitoring, diabetes care, blood glucose levels",
  image = "/glucosemeter-svgrepo-com.svg",
  url = "https://your-domain.com",
  type = "website",
  author = "Glucose Tracker",
  publishedTime,
  modifiedTime,
  section,
  tags = [],
}: SEOProps) {
  const fullTitle =
    title === "Glucose Tracker - Diabetes Management Made Simple"
      ? title
      : `${title} | Glucose Tracker`;

  return (
    <Helmet>
      {/* Primary Meta Tags */}
      <title>{fullTitle}</title>
      <meta name="title" content={fullTitle} />
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      <meta name="author" content={author} />

      {/* Open Graph / Facebook */}
      <meta property="og:type" content={type} />
      <meta property="og:url" content={url} />
      <meta property="og:title" content={fullTitle} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={image} />
      <meta property="og:site_name" content="Glucose Tracker" />
      <meta property="og:locale" content="en_US" />
      {publishedTime && (
        <meta property="article:published_time" content={publishedTime} />
      )}
      {modifiedTime && (
        <meta property="article:modified_time" content={modifiedTime} />
      )}
      {section && <meta property="article:section" content={section} />}
      {tags.map((tag, index) => (
        <meta key={index} property="article:tag" content={tag} />
      ))}

      {/* Twitter */}
      <meta property="twitter:card" content="summary_large_image" />
      <meta property="twitter:url" content={url} />
      <meta property="twitter:title" content={fullTitle} />
      <meta property="twitter:description" content={description} />
      <meta property="twitter:image" content={image} />

      {/* Canonical URL */}
      <link rel="canonical" href={url} />
    </Helmet>
  );
}
